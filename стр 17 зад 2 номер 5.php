<?php
$N = (int)readline("Введите трехзначное число: ");

if ($N >= 100 && $N <= 999) {
    $reversedNumber = ($N % 10) * 100 + ((int)($N / 10) % 10) * 10 + (int)($N / 100);
    echo "Число в обратном порядке: " . $reversedNumber . "\n";
} else {
    echo "Ошибка: введено не трехзначное число.\n";
}
?>